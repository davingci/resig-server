	/**
	 * @api {post} /blog/add Add Blog
	 * @apiVersion 0.1.0
	 * @apiName AddBlog
	 * @apiGroup Blog
	 * @apiPermission authorization
	 * @apiDescription add blog
	 * 
	 * @apiUse Header
	 * @apiParam {String} title blog title
	 * @apiParam {String} html html content, usually generated by editor in(for example, ckeditor,Editor.md) front-end.
	 * @apiParam {String} markdown markdown content if you use markdown editor in front-end, if not, it should be "" 
	 * @apiParam {String="htmlEditor","markdownEditor"} blogEditor the editor which you used in front-end. The value should be htmlEditor or markdownEditor.
	 
	 * @apiUse Success
	 * @apiSuccessExample Success-Response:
	 *     HTTP/1.1 200 OK
	 *     {
	 *       "code": 200,
	 *       "message": "publish blog success.",
	 *       "data":""
	 *     }
	 
	 * @apiUse Unauthorized
	 */
	@Secured
	@POST
	@Path("/add")
	@Produces(MediaType.APPLICATION_JSON+ ";charset=utf-8")
	public Response add(@FormParam("title") String title, 
						@FormParam("html") String html,
						@FormParam("markdown") String markdown,
						@FormParam("blogEditor") String blogEditor) {
		
		Date createAt = CalendarUtil.getNow();
		Date lastModifyAt = CalendarUtil.getNow();
		
		AuthPrincipal principal = (AuthPrincipal) securityContext.getUserPrincipal();

	    String username = principal.getUsername();
	    int userId = principal.getUserId();
	    
	    //TODO
	    //Extra content from html
	    //index with ElasticSearch
		
		Blog blog = new Blog.BlogBuilder()
							.title(title)
							.markdown(markdown)
							.html(html)
							.blogEditor(blogEditor)
							.userId(userId)
							.username(username)
							.createAt(createAt)
							.lastModifyAt(lastModifyAt)
							.build();
		HibernateDao dao = new HibernateDao();
		dao.add(blog);
		
		
		
		ResponseUtil ru = new ResponseUtil();
		ru.code(200).message("publish blog success.");
		return Response.status(200).entity(ru).build();
	}
	
	/**
	 * @api {post} /blog/update Update Blog
	 * @apiVersion 0.1.0
	 * @apiName UpdateBlog
	 * @apiGroup Blog
	 * @apiPermission authorization
	 * @apiDescription update blog
	
	 * @apiUse Header
	 * @apiParam {String} title blog title
	 * @apiParam {String} html html content, usually generated by editor in(for example, ckeditor,Editor.md) front-end.
	 * @apiParam {String} markdown markdown content if you use markdown editor in front-end, if not, it should be "" 
	 * @apiParam {String} blogId the id of the blog which you would like to update.
	 
	 * @apiUse Success
	 * @apiSuccessExample Update Success:
	 *     HTTP/1.1 200 OK
	 *     {
	 *       "code": 200,
	 *       "message": "update blog success.",
	 *       "data":""
	 *     }
	 * @apiSuccessExample Update Failure:
	 *     HTTP/1.1 200 OK
	 *     {
	 *       "code": 201,
	 *       "message": "blogId is incorrect.",
	 *       "data":""
	 *     }
	 * @apiUse Unauthorized
	 */
	@Secured
	@POST
	@Path("/update")
	@Produces(MediaType.APPLICATION_JSON+ ";charset=utf-8")
	public Response add(@FormParam("title") String title, 
						@FormParam("html") String html,
						@FormParam("markdown") String markdown,
						@FormParam("blogId") int blogId) {
		
		Date lastModifyAt = CalendarUtil.getNow();
		
		HibernateDao dao = new HibernateDao();
		Blog blog = (Blog) dao.findById(Blog.class, blogId);
		
		ResponseUtil ru = new ResponseUtil();
		
		if(blog != null) {
			blog.setTitle(title);
			blog.setHtml(html);
			blog.setMarkdown(markdown);
			blog.setLastModifyAt(lastModifyAt);
			dao.update(blog);		
			ru.code(200).message("update blog success.");
			
		}
		else {
			ru.code(201).message("blogId is incorrect.");
			
		}
		return Response.status(200).entity(ru).build();
	}
	
