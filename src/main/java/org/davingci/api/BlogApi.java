	@Secured
	@POST
	@Path("/delete")
	@Produces(MediaType.APPLICATION_JSON+ ";charset=utf-8")
	public Response delete(@FormParam("blogId") int blogId) {
		ResponseUtil ru = new ResponseUtil();
		HibernateDao dao = new HibernateDao();
		Blog blog = (Blog) dao.findById(Blog.class, blogId);
		if(blog != null) {
			dao.delete(blog);
			ru.code(200).message("delete blog success.");
			
		}
		else {
			ru.code(201).message("blogId is incorrect.");
			
		}
		return Response.status(200).entity(ru).build();
		
	}
	
	/**
	 * @api {post} /blog/query Query Blog
	 * @apiVersion 0.1.0
	 * @apiName QueryBlog
	 * @apiGroup Blog
	 * @apiPermission all
	 * @apiDescription query blog by createTime
	
	 * @apiParam {String} startTime the bolgs should be created after the startTime
	 * @apiParam {String} endTime endTime the blogs should be created before the endTime
	 * @apiParamExample {String} Request-Example:
	 * startTime:"2017-11-09",
	 * endTime:"2017-11-20",
	 * @apiUse Success
	 * @apiSuccessExample Query Success:
		HTTP/1.1 200 OK
		{
		    "code": 200,
		    "data": [
		        {
		            "blogEditor": "",
		            "blogId": 2,
		            "createAt": 1510934967000,
		            "favouriteCount": 1,
		            "html": "",
		            "lastModifyAt": 1510934967000,
		            "markCount": 1,
		            "markdown": "",
		            "title": "admin",
		            "userId": 1,
		            "username": "huangdongxu",
		            "viewCount": 0
		        },
		        {
		            "blogEditor": "",
		            "blogId": 4,
		            "createAt": 1510944250000,
		            "favouriteCount": 0,
		            "html": "<html><p>admimi</p></html>",
		            "lastModifyAt": 1511365630000,
		            "markCount": 0,
		            "markdown": "",
		            "title": "admim",
		            "userId": 1,
		            "username": "huangdongxu",
		            "viewCount": 0
		        }
		    ],
		    "message": "success."
		} 
	*/
	@POST
	@Path("/query")
	@Produces(MediaType.APPLICATION_JSON+ ";charset=utf-8")
	public Response query(@FormParam("startTime") String startTime, @FormParam("endTime") String endTime) {
		SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
		Date startDate = new Date();
		Date endDate = new Date();
		try {
			startDate = sdf.parse(startTime);
			endDate = sdf.parse(endTime);
		} catch (ParseException e) {
			
			e.printStackTrace();
		}
		ArrayList<Date> list = new ArrayList<Date>();
		list.add(startDate);
		list.add(endDate);
		
		HibernateDao dao = new HibernateDao();
		String hql = "FROM Blog B WHERE B.createAt between ? and ?";
		List<?> blogs = dao.findByHQL(hql,list);
		
		
		
		ResponseUtil ru = new ResponseUtil();
		ru.code(200).message("success.").data(blogs);
		return Response.status(200).entity(ru).build();
	}
